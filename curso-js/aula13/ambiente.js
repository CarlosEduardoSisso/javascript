 // ESTRUTURA DE REPETIÇÃO COM TESTE LÓGICO NO INÍCIO
var c = 1
while (c <= 6) {
    console.log(`Teste lógico no início nº ${c}`)
    c++
}

// ESTRUTURA DE REPETIÇÃO COM TESTE LÓGICO NO FINAL
var c = 1
do {
    console.log(`Teste lógico no final nº ${c}`)
    c++
} while (c <= 6)

// ESTRUTURA DE REPETIÇÃO COM VARIÁVEL DE CONTROLE
for (var c = 1;c <= 6;c++) {
    console.log(`Teste lógico com variável de controle nº ${c}`)
}